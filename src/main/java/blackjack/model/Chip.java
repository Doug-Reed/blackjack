package blackjack.model;

public class Chip {
    private String color;
    private int amount;

    public void setColor(String color) {
        this.color = color;
    }

    public String getColor() {
        return this.color;
    }

   public void setColor(int amount) {
        this.amount = amount;
    }

    public int getAmount() {
        return this.amount;
    }
}